export default function Home() {
  const { ContractFactory, NFTYacht, provider } = useContextAPI();

  const [State, SetState] = useImmer({
    data: []
  });

  useEffect(() => {
    const run = async () => {
      let addresses;
      try {
        addresses = await ContractFactory.getAllContractAddress();
      } catch (e) {
        console.log(e);
      }

      for (let i = 0; i < addresses.length; i++) {
        const ContractUSDT = new ethers.Contract(
          addresses[i],
          NFTYacht,
          provider
        );

        const name = await ContractUSDT.name();
        const symbol = await ContractUSDT.symbol();

        const date = {
          id: i,
          name: name,
          symbol: symbol,
          address: addresses[i],
          imageSrc:
            "https://tailwindui.com/img/ecommerce-images/product-page-01-related-product-01.jpg",
          imageAlt: "Front of men's Basic Tee in black."
        };

        SetState(draft => {
          draft.data.push(date);
        });
      }
    };
    run();
  }, []);

  return (
    <div className="bg-white">
      <div className="max-w-2xl mx-auto py-16 px-4 sm:py-24 sm:px-6 lg:max-w-7xl lg:px-8">
        <h2 className="text-2xl font-extrabold tracking-tight text-gray-900">
          Home
        </h2>

        <div className="mt-6 grid grid-cols-1 gap-y-10 gap-x-6 sm:grid-cols-2 lg:grid-cols-4 xl:gap-x-8">
          {State.data.map(Contract => (
            <Link
              to={`/contract/${Contract.address}`}
              key={Contract.id}
              className="group relative"
            >
              <div className="w-full min-h-80 bg-gray-200 aspect-w-1 aspect-h-1 rounded-md overflow-hidden group-hover:opacity-75 lg:h-80 lg:aspect-none">
                <img
                  src={Contract.imageSrc}
                  alt={Contract.imageAlt}
                  className="w-full h-full object-center object-cover lg:w-full lg:h-full"
                />
              </div>
              <div className="mt-4 flex justify-between">
                <div>
                  <h3 className="text-sm text-gray-700">
                    <a href="#">
                      <span aria-hidden="true" className="absolute inset-0" />
                      {Contract.name}
                    </a>
                  </h3>
                </div>
              </div>
            </Link>
          ))}
        </div>
      </div>
    </div>
  );
}
